package CMath;

public class AMath {
    public static void main(String[] args) {
        //向上取整
        System.out.println(Math.ceil(19.658));

        //向下取整
        System.out.println(Math.floor(19.658));

        //四舍五入 返回整数
        System.out.println(Math.round(19.358));

        //绝对值
        System.out.println(Math.abs(-1));

        //随机数 0-1
        System.out.println(Math.random());

        //https://www.matools.com/api/java8
    }
}
